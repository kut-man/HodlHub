function transformData(): { time_open: string; rate_open: number }[] {
  return chartData.map((item) => {
    const { time_open, rate_open } = item;
    const formattedTime = formatTimestampToReadableDay(time_open);

    return {
      time_open: formattedTime,
      rate_open: rate_open,
    };
  });
}

function formatTimestampToReadableDay(timestamp: string): string {
  const options: Intl.DateTimeFormatOptions = {
    day: "numeric",
    month: "short",
  };

  const date = new Date(timestamp);
  const formatter = new Intl.DateTimeFormat("en-US", options);

  return formatter.format(date);
}
